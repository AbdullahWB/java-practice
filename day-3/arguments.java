public class arguments {
    public static void main(String[] args) {
        System.out.println(args[0]);
        System.out.println(args[1]);
        System.out.println(args[2]);
    }
}

/* 
 * javac arguments.java
 * java arguments // pass the arguments in here like
 * java arguments abdullah is sad person 
 * here ever space means that java new arguments
 * like abdullah args[0]
 *  is args[1]
 *  sad args[2]
 *  person args[3]
 */