public class char_var {
    public static void main(String[] args) {
        char c1 = 'a';
        char c2 = 67;
        char c3 = '\u0047';
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);

        int c4 = 'a';
        int c5 = '\u0048';
        System.out.println(c4);
        System.out.println(c5);
    }
}
