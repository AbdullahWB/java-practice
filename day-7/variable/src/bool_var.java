public class bool_var {
    public static void main(String[] args) {
        boolean b1 = true;
        boolean b2 = false;
        boolean b3 = 3 > 2;
        boolean b4 = 3 < 2;

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);

        if(b4){
            System.out.println("b4 is true");
        }else{
            System.out.println("b4 is false");
        }
    }
}
